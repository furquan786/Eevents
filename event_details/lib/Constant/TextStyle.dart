import 'package:flutter/cupertino.dart';

TextStyle boldstyle(Color color, double size) {
  return TextStyle(
    fontSize: size,
    color: color,
    fontWeight: FontWeight.w500,
  );
}

TextStyle ligthtstyle(Color color, double size) {
  return TextStyle(
    fontSize: size,
    color: color,
    fontWeight: FontWeight.w400,
  );
}
